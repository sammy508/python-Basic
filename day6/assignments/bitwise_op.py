x = int(input("inter x : "))
y = int(input("inter y: "))

result = x & y
print (result, "\n")

result = x | y
print (result, "\n")

result = x ^ y
print (result, "\n")

result = x << y
print (result, "\n")

result = x >> y
print (result, "\n")